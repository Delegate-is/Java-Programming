public class comments {
    public static void main(String[] args) {
        int a; // This is a variable declaration
        a = 10; // This is an assignment statement
        a = 40; // This is another assignment statement
        final int b = 20; // This is a constant declaration
        //b = 30; // This will cause a compilation error because b is final
        final double PI = 3.14; // This is a constant declaration
        System.out.println("Value of a is: "+ a + " and value of b is: " + b);
        System.out.println("Value of a PI: "+ PI);
        /*
         * This is a multiline comment.
         * This code is written in Java by Manish
         */
    }
}